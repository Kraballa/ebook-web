<h3>Description</h3>
<div>
    <it>ebook-web</it> is a minimal ebook reader for the browser.
</div>
<h3>Usage</h3>
<div>
    Simply click the button and load up an ebook (.epub). A list of numbered buttons should appear that represent the
    different chapter files. Embedded styles and images should appear automatically.
</div>

